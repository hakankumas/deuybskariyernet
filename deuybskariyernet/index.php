          <h2>HAKKIMIZDA</h2>
        </div>
        <div class="row content">
          <div class="col-lg-6">
            <p>deuybskariyer.net, Dokuz Eylül Üniversitesi Yönetim Bilişim Sistemleri bölümü Lisans öğrencileri için hazırlanmış yetenek havuzudur.</p>
            <p>Yönetim Bilişim Sistemleri, işletme ve bilişim konularını bir arada işleyen bir alandır.</p>
            <p>Günümüzde bilişim alanındaki gelişmelerin işletme bilimine kattığı yeniliklerle çok daha etkin ve verimli karar verme süreçleri oluşmuştur. Ayrıca, bilişim sistemlerinin yönetimi de başlı başına bir yönetim konusu haline gelmiştir. Yönetim Bilişim Sistemleri’ni ilgilendiren konular, bu iki alanın etkileşimi ve entegrasyonu ile ortaya çıkmıştır.</p>
            <p>Programdan mezun olan kişilerin yöneticilerin karar süreçlerini en iyilemelerinde kullanacakları en güncel bilişim sistem ve teknolojilerine hakim olmalarının yanında, analitik düşünme ve organizasyonun yapısı ve yönetimi hakkında yeterli bilgiye sahip olmaları amaçlanmaktadır.</p>
            <p>Bu programın avantajı işletmelerde bilişim sistemlerini geliştirecek düzeyde bilişim ve bu sistemleri yönetecek düzeyde işletme bilgisine sahip öğrenciler yetiştirmektir.</p>
            <p>Program, mezunlarının teknik, analitik, yönetsel düşünme ve iletişim yeteneklerine odaklanmaktadır.</p>
            
          </div>
          <div class="col-lg-6 pt-4 pt-lg-0">
            <p>Programda okuyan öğrenciler aşağıdaki konularda donanımlı olacak şekilde yetiştirileceklerdir:</p>
            <ul>
              <li><i class="ri-check-double-line"></i> Bilişim teknolojileri temellerine hakim olma</li>
              <li><i class="ri-check-double-line"></i> Yazılım geliştirme mantığına hakim olma</li>
              <li><i class="ri-check-double-line"></i> Telekomünikasyon Ağları temellerini anlama ve yönetme</li>
              <li><i class="ri-check-double-line"></i> Bilişim sistemleri analizi ve tasarımını gerçekleştirme</li>
              <li><i class="ri-check-double-line"></i> Veritabanı sistemleri analizi ve tasarımını gerçekleştirme</li>
              <li><i class="ri-check-double-line"></i> Kurumsal sistemler ve tedarik zinciri sistemlerinin faydalarına hakim olma</li>
              <li><i class="ri-check-double-line"></i> Yazılım geliştirme sürecini izleme ve kontrol etme</li>
              <li><i class="ri-check-double-line"></i> Teknik bilgisi olmayan kullanıcılar ve üst yönetim ile iletişim sayesinde gereksinimleri belirleme</li>
              <li><i class="ri-check-double-line"></i> Gereksinimleri tasarıma dönüştürme</li>
              <li><i class="ri-check-double-line"></i> Kullanıcı isteklerini bilişim sistemlerine yansıtma</li>
